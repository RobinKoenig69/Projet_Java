package Database_access;


public final class Exceptions_Database extends Exception {

    public Exceptions_Database() {
        super();
    }

    public Exceptions_Database(String message) {
        super(message);
    }

    public Exceptions_Database(String message, Throwable cause) {
        super(message, cause);
    }

    public Exceptions_Database(Throwable cause) {
        super(cause);
    }
}
